public class Asterics{
 public static void main(String[] args){
        for(int i = 0;i<=10;i++){
        String result = "* ".repeat(i);
        System.out.println(result);
        }
        for(int i = 10;i>=0;i--){
        String result = "* ".repeat(i);
        System.out.println(result);
        }
        for(int i = 0;i<=10;i++){
        String result = "*".repeat(i);
        System.out.println(result);
        }
        for(int i = 0;i<=10;i++){
        String result = "* ".repeat(i);
        System.out.println(result);
        }
    }
}
